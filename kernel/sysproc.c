extern int freemem(void);
extern int procnum(void);

uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_trace(void)
{
  struct proc *p = myproc();
  p->trace_mask = 0;
  argint(0, &(p->trace_mask));
  // p->traced = 1;
  return 0;
}

uint64
sys_sysinfo(void)
{
  struct proc *p = myproc();
  uint64 info_addr;
  if (argaddr(0, &info_addr) < 0) {
    return -1;
  }
  int free_mem = freemem();
  int proc_num = procnum();
  struct sysinfo sys_info;
  sys_info.freemem = free_mem;
  sys_info.nproc = proc_num;
  if (copyout(p->pagetable, info_addr, (char *)&sys_info, sizeof(sys_info)) < 0) {
    return -1;
  }
  return 0;
}